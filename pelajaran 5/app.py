# operasi artimatika

a = 10
b = 3

# penjumlahan
hasil = a + b
print(a, "+", b, "=", hasil)

# pengurangan
hasil = a - b
print(a, "-", b, "=", hasil)

# perkalian
hasil = a * b
print(a, "*", b, "=", hasil)

# pembagian
hasil = a / b
print(a, "/", b, "=", hasil)

# pembagian bulat ( floor division )
hasil = a // b
print(a, "//", b, "=", hasil)

# sisa bagi ( modulus )
hasil = a % b
print(a, "%", b, "=", hasil)

# pangkat
hasil = a ** b
print(a, "**", b, "=", hasil)